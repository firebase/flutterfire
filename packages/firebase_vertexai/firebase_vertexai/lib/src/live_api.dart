  // ignore: public_member_api_docs
  aoede('Aoede'),

  // ignore: public_member_api_docs
  charon('Charon'),

  // ignore: public_member_api_docs
  fenrir('Fenrir'),

  // ignore: public_member_api_docs
  kore('Kore'),

  // ignore: public_member_api_docs
  leda('Leda'),

  // ignore: public_member_api_docs
