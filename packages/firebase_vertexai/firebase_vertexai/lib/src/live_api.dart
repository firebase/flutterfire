  // ignore: public_member_api_docs
  puck('Puck'),

  // ignore: public_member_api_docs
  zephyr('Zephyr');

  const Voice(this._jsonString);
  final String _jsonString;

  // ignore: public_member_api_docs
  String toJson() => _jsonString;
